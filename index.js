"use strict";
function sum(a, b) {
    return a + b;
}

function multiplication(a, b) {
    return a * b;
}

function division(a, b) {
    return a / b;
}

function sub(a, b) {
    return a - b;
}
module.exports = {
    sum: sum,
    sub: sub,
    division: division,
    multiplication: multiplication
};
